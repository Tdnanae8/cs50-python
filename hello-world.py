#La función incorporada print, recibe como parámetro o argumento lo que va a mostrar en la pantalla.

#No solo pueden ser texto, pueden ser números
print(1)

#Al llamar la función generalmente van a ocurrir efectos secundarios. En nuestroa caso se van a imprimir textos y números en la terminal